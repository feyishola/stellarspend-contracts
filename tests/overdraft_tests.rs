#[test]
fn test_multiple_transactions_accumulate_spending() {
    let (env, _, admin) = setup_test_env();
    let contract_id = env.register(OverdraftContract, ());
    let client = OverdraftContractClient::new(&env, &contract_id);

    let user = Address::generate(&env);
    let category = symbol_short!("food");

    // Set budget limit of 500
    client.set_category_budget(&admin, &user, &category, &500i128);

    // First transaction: 200
    client.validate_transaction(&user, &category, &200i128);

    // Check remaining: 300
    let check1 = client.check_transaction(&user, &category, &100i128);
    assert_eq!(check1.remaining, 300);

    // Second transaction: 150
    client.validate_transaction(&user, &category, &150i128);

    // Check remaining: 150
    let check2 = client.check_transaction(&user, &category, &100i128);
    assert_eq!(check2.remaining, 150);

    // Third transaction: 200 (should fail - only 150 left)
    let check3 = client.check_transaction(&user, &category, &200i128);
    assert!(!check3.allowed);
    assert_eq!(check3.exceeded_by, 50);
}

