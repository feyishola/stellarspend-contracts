#[test]
fn test_different_categories_independent() {
    let (env, _, admin) = setup_test_env();
    let contract_id = env.register(OverdraftContract, ());
    let client = OverdraftContractClient::new(&env, &contract_id);

    let user = Address::generate(&env);

    // Set different limits for different categories
    client.set_category_budget(&admin, &user, &symbol_short!("food"), &500i128);
    client.set_category_budget(&admin, &user, &symbol_short!("transport"), &200i128);
    client.set_category_budget(&admin, &user, &symbol_short!("entertainment"), &100i128);

    // Spend from food
    client.validate_transaction(&user, &symbol_short!("food"), &400i128);

    // Spend from transport
    client.validate_transaction(&user, &symbol_short!("transport"), &150i128);

    // Verify each category tracks spending independently
    assert_eq!(
        client
            .get_category_budget(&user, &symbol_short!("food"))
            .unwrap()
            .spent,
        400
    );
    assert_eq!(
        client
            .get_category_budget(&user, &symbol_short!("transport"))
            .unwrap()
            .spent,
        150
    );
    assert_eq!(
        client
            .get_category_budget(&user, &symbol_short!("entertainment"))
            .unwrap()
            .spent,
        0
    );

    // Check remaining in each
    assert_eq!(
        client
            .check_transaction(&user, &symbol_short!("food"), &200i128)
            .remaining,
        100
    );
    assert_eq!(
        client
            .check_transaction(&user, &symbol_short!("transport"), &100i128)
            .remaining,
        50
    );
    assert_eq!(
        client
            .check_transaction(&user, &symbol_short!("entertainment"), &100i128)
            .remaining,
        100
    );
}

