#[test]
fn test_overdraft_event_emitted_check() {
    let env = Env::default();
    env.ledger().set_timestamp(1000);

    let contract_id = env.register(OverdraftContract, ());
    let client = OverdraftContractClient::new(&env, &contract_id);

    let admin = Address::generate(&env);
    client.initialize(&admin);

    let user = Address::generate(&env);
    let category = symbol_short!("food");

    client.set_category_budget(&admin, &user, &category, &100i128);

    // Clear events
    let _ = env.events().all();

    // Trigger overdraft attempt
    let _ = env.try_invoke_contract::<OverdraftContractClient>(
        &contract_id,
        "validate_transaction",
        (&user, &category, &150i128),
    );

    // Check events were emitted
    let events = env.events().all();
    assert!(events.len() > 0);

    // Verify overdraft event was published
    let overdraft_events: Vec<_> = events
        .iter()
        .filter(|e| {
            if let Some(topics) = e.topic().as_vec() {
                topics.len() >= 2 && topics.get(0).unwrap() == symbol_short!("overdraft")
            } else {
                false
            }
        })
        .collect();

    assert!(overdraft_events.len() > 0);
}

